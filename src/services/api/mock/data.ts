const DAY = 86400;

const getNextAiringAt = (dayOffset: number, hour: number, minute: number): number => {
    const now = new Date();
    const today = new Date(now.getFullYear(), now.getMonth(), now.getDate(), hour, minute);
    const target = new Date(today.getTime() + dayOffset * DAY * 1000);
    if (target.getTime() < now.getTime()) {
        target.setTime(target.getTime() + 7 * DAY * 1000);
    }
    return Math.floor(target.getTime() / 1000);
};

